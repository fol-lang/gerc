    // 7.9: deterministic ordering
    #[test]
    fn emit_deterministic_ordering() {
        let proj = make_projection(vec![
            RustItem::Constant(RustConstant {
                name: "A".into(),
                ty: RustType::CInt,
                value: "1".into(),
                doc: None,
            }),
            RustItem::TypeAlias(RustTypeAlias {
                name: "B".into(),
                target: RustType::CInt,
                doc: None,
            }),
            RustItem::Record(RustRecord {
                name: "C".into(),
                kind: RustRecordKind::Struct,
                fields: vec![],
                is_opaque: false,
                doc: None,
            }),
            RustItem::Function(RustFunction {
                name: "D".into(),
                parameters: vec![],
                return_type: RustType::Void,
                variadic: false,
                doc: None,
            }),
        ]);
        let src1 = emit_source(&proj);
        let src2 = emit_source(&proj);
        assert_eq!(src1, src2, "emission not deterministic");

        // Order: constants, type aliases, enums, records, extern block
        let pos_const = src1.find("pub const A").unwrap();
        let pos_alias = src1.find("pub type B").unwrap();
        let pos_struct = src1.find("pub struct C").unwrap();
        let pos_extern = src1.find("extern \"C\"").unwrap();
        assert!(pos_const < pos_alias);
        assert!(pos_alias < pos_struct);
        assert!(pos_struct < pos_extern);
    }

